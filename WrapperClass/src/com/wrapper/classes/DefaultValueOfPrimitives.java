package com.wrapper.classes;

public class DefaultValueOfPrimitives {

	public static void main(String[] args) {
		
		int i = 0;
		double d = 0;
		boolean b = false;
		String s = null;
		char c = 0;
		short sort = 0;
		long l = 0;
		
		System.out.println("Default value of int:::-"+i);
		System.out.println("Default value of double:::-"+d);
		System.out.println("Default value of boolean:::-"+b);
		System.out.println("Default value of String:::-"+s);
		System.out.println("Default value of char:::-"+c);
		System.out.println("Default value of Short:::-"+sort);
		System.out.println("Default value of long:::-"+l);
		
		
	}
}
